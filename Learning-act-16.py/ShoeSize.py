# Variable set to Float
my_shoe_size = 10.0

print("The type of my_shoe_size ", type(my_shoe_size))
print("My shoe size is ", my_shoe_size)

print("Casting to a string...")
# Variable cast to string
my_shoe_size = str(my_shoe_size)

print("The type of my_shoe_size is now ", type(my_shoe_size))
print("My shoe size is ", my_shoe_size)